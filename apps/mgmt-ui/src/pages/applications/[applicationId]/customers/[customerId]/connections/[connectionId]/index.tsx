type EntityObjectMappingProps = {
  entity: string;
  providerName: string;
  object?: string;
  setObject: (selected: string | undefined) => void;
};
function EntityObjectMapping({ entity, providerName, object, setObject }: EntityObjectMappingProps) {
  const objectOptions = getStandardObjectOptions(providerName);
  return (
    <>
      <Grid item xs={4}>
        <Stack direction="row" className="gap-2 items-center justify-between">
          <Stack direction="row" className="gap-2 items-center">
            {providerToIcon('supaglue', 40)}
            <Typography>{entity}</Typography>
          </Stack>
          <SyncAltIcon />
        </Stack>
      </Grid>
      <Grid item xs={8}>
        <Stack direction="row" className="gap-4 items-start">
          {providerToIcon(providerName, 40)}
          <Autocomplete
            size="small"
            id="entity-object"
            options={objectOptions}
            defaultValue={object}
            autoSelect
            renderTags={(value: readonly string[], getTagProps) =>
              value.map((option: string, index: number) => (
                <Chip variant="outlined" label={option} {...getTagProps({ index })} />
              ))
            }
            renderInput={(params) => (
              <TextField {...params} label="Standard objects" helperText={`Available objects in ${providerName}.`} />
            )}
            onChange={(event: any, value: string | null) => {
              setObject(value ?? undefined);
            }}
          />
        </Stack>
      </Grid>
    </>
  );
}

type EntityFieldMappingsProps = {
  customerId: string;
  entity: string;
  object: string;
  providerName: string;
  fields: EntityFieldMapping[];
  setFieldMapping: (entityField: string, mappedField: string | undefined) => void;
};

function EntityFieldMappings({
  customerId,
  entity,
  providerName,
  object,
  fields,
  setFieldMapping,
}: EntityFieldMappingsProps) {
  const { properties, isLoading } = useProperties(customerId, providerName, 'standard', object);
  return (
    <>
      {fields.map(({ entityField, mappedField }, idx) => (
        <>
          <Grid item xs={4}>
            <Typography>{entityField}</Typography>
          </Grid>
          <Grid item xs={12}>
            <Autocomplete
              disabled={!object || isLoading}
              size="small"
              id="entity-object-field"
              options={properties ?? []}
              defaultValue={mappedField}
              autoSelect
              renderTags={(value: readonly string[], getTagProps) =>
                value.map((option: string, index: number) => (
                  <Chip variant="outlined" label={option} {...getTagProps({ index })} />
                ))
              }
              renderInput={(params) => (
                <TextField
                  {...params}
                  label={`${providerName} field`}
                  helperText={`Available fields for ${object} in ${providerName}.`}
                />
              )}
              onChange={(event: any, value: string | null) => {
                setFieldMapping(entityField, value ?? undefined);
              }}
            />
          </Grid>
        </>
      ))}
    </>
  );
}
